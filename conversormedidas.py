print("Insira uma distância em metros")

distancia_metros = float(input("Distância em metros:"))
distancia_centimentros = distancia_metros * 100
distancia_milimetros = distancia_metros * 1000

print(f"A distância de {distancia_metros} metros é igual a {distancia_centimentros} centímetros e {distancia_milimetros} milímetros")
